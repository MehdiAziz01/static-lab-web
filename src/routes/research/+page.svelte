    <h1 class="section-title">Research</h1>
    
    <h2 class="subsection-title">Exploring Nanotechnology</h2>
    <p class="subsection-text">
        Our lab focuses on several key research areas:
    </p>
    <ul class="information-list">
        <li>
            <strong>Multi-Analyte Sensor Array Platforms:</strong> Developing systems capable of simultaneous, in-situ detection of multiple biomarkers, paving the way for more efficient diagnostics and monitoring.
        </li>
        <li>
            <strong>Carbon Nanotube-Based Biosensors:</strong> Innovating covalent functionalization techniques for carbon nanotube forests on metal-silicon chips, enhancing sensitivity and specificity in biosensing applications.
        </li>
        <li>
            <strong>Custom Electronics and Software:</strong> Designing specialized electronics and data processing software to support our sensor technologies, ensuring accurate and reliable results.
        </li>
    </ul>

    <h2 class="subsection-title">Significance and Impact</h2>
    <p class="subsection-text">
        Our research holds significant potential for:
    </p>
    <ul class="information-list">
        <li>
            <strong>Healthcare Monitoring:</strong> Enabling early detection of diseases through advanced biosensors.
        </li>
        <li>
            <strong>Energy Harvesting and Storage:</strong> Improving efficiency in energy systems with nano-engineered materials.
        </li>
        <li>
            <strong>Environmental Sensing:</strong> Providing precise monitoring of environmental conditions and pollutants.
        </li>
        <li>
            <strong>Semiconductor Industry:</strong> Advancing fabrication techniques and materials for next-generation devices.
        </li>
    </ul>

