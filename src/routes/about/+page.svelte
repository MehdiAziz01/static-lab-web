    <h1 class="section-title">About Us</h1>
    
    <h2 class="subsection-title">Our Mission</h2>
    <p class="subsection-text">
        At the UC Berkeley Nanotechnology Lab, we are committed to pioneering innovations in nanotechnology with a focus on practical applications. Our goal is to develop advanced sensor technologies and systems that can transform healthcare, enhance energy efficiency, improve environmental monitoring, and revolutionize the semiconductor industry.
    </p>
    
    <h2 class="subsection-title">Who We Are</h2>
    <p class="subsection-text">
        Under the leadership of Dr. Waqas Khalid, an Associate Scientist at the California Institute for Quantitative Biosciences (QB3), our team brings together experts in nanotechnology, materials science, and electrical engineering. We are dedicated to pushing the boundaries of what's possible through interdisciplinary research and collaborative partnerships.
    </p>

    <h2 class="subsection-title">Our teams</h2>
    <p class="subsection-text">
        Our lab consists of several specialized teams working on cutting-edge research projects:
    </p>
    <ul class="information-list">
        <li><strong>Biosensor Technology Team</strong> - Developing novel biomolecule sensors for healthcare applications with advanced impedance spectroscopy technology.</li>
        <li><strong>Analysis Team</strong> - Integrating custom analysis frameworks with large language models for biomolecular analysis and research support.</li>
        <li><strong>Drone Team</strong> - Advancing aerial vehicle technology through ultracapacitor energy storage integration.</li>
        <li><strong>Microfluidics Team</strong> - Pioneering fluid control through carbon nanotubes for enhanced biological analysis.</li>
        <li><strong>Energy Storage Team</strong> - Developing sustainable carbon-based ultracapacitors as alternatives to traditional batteries.</li>
        <li><strong>Exoskeleton Team</strong> - Creating neurally connected body-integrated systems for rehabilitation.</li>
        <li><strong>Lithography Team</strong> - Working to democratize lithography through carbon nanotube modulation.</li>
        <li><strong>Plasma Jet Team</strong> - Developing focused plasma streams for energy and cutting applications.</li>
        <li><strong>Design Team</strong> - Creating intuitive and efficient designs for our technological innovations.</li>
    </ul>
